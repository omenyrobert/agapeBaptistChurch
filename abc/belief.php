                  <h4>
Agape Baptist Church subscribes to Seven Baptist distinctives that make us truly Baptist. We strongly affirm our commitment to biblical truth and to these distinctives that define our name as Baptists. This simple acrostic (B-A-P-T-I-S-T) helps to sum up the core distinctives of who we are:</h4>

 
<h4>
Biblical Authority in all matters of faith and practice. We believe the Bible is inspired and infallible and is the final authority. It is from God’s Word that we understand and teach the fundamental doctrines of our faith as well as pattern our church polity (2 Timothy 3:16; John 17:17; Acts 17:11; Hebrews 4:12; 2 Peter 1:20–21).
</h4>
 
<h4>
Autonomy or self-governing power of the local church. We believe that the local church should be independent of a hierarchical framework or outside governmental structure (Colossians 1:18; Acts 13–14, 20:19–30; Ephesians 1:22–23).
</h4>
 
<h4  >
Priesthood of believers. God’s Word assures believers that we have direct access to God through our relationship with Christ. We believe and teach that the priesthood of the believer is the unspeakably precious privilege of every child of God (Hebrews 4:14–16; 1 Timothy 2:5–6; 1 Peter 2:5–10).</h4>


<h4>
Two Ordinances. The local church should practice two ordinances: (1) baptism of believers by immersion in water, identifying the individual with Christ in His death, burial, and resurrection, and (2) the Lord’s Supper, or communion, commemorating His death for our sins (Matthew 28:19, 20; 1 Corinthians 11:23-32).
</h4>
 
<h4>
Individual Soul Liberty. We believe that each person must make a personal decision of repentance and faith in Christ (Romans 10:9–17, 14:1–23).
</h4>
 
<h4>
Saved, Baptized Church Membership. We believe that local church membership should be restricted to those who have a personal testimony of faith in Jesus Christ and who have obediently followed the Lord in believer’s baptism. (Acts 2:41-47; 1 Corinthians 12:12; 2 Corinthians 6:14; Ephesians 4:3).
</h4>
 
<h4>
Two offices within the church. Scripture only mentions two church offices—pastor (also referred to as elder or bishop) and deacon. These two offices are to be filled by godly leaders of integrity in the local church (Philippians 1:1; Acts 6:1–7; 1 Timothy 3:1–13; Titus 1:6–9; 1 Peter 5:1–4.)

                  </h4>
                    
